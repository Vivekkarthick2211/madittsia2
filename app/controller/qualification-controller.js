var qualification=require('../model/qualification-model')

exports.quali=(req,res)=>{
    qualification.qualify((err,data)=>{
        if(err){
            console.log(err)
            res.send({
                status:400,
                msg:err
            })
        }
        res.send({
            status:200,
            msg:data
        })
    })
}

